//Find the average of array elements.

let marks = [85, 97, 44, 37, 76, 60];
let sum = 0;
for (let i of marks) {
    sum += i;
}
let avg = sum / marks.length;

console.log("Average marks of all the class = ",avg);


// Average nikalne ka formula:- Sum of total number divided by total number of value.